import org.mockito.Mockito.mock
import org.mockito.Mockito.verify

@RunWith(AndroidJUnit4::class)
@ExperimentalCoroutinesApi
//UI Testing
@MediumTest
class ReminderListFragmentTest : AutoCloseKoinTest() {

    private lateinit var repository: ReminderDataSource

    private val reminder = ReminderDTO(
        "title",
        "description",
        "location",
        42.97419826734443,
        -85.68307958930033
    )

    @Before
    fun init() {
        stopKoin()

        val myModule = module {
            viewModel {
                RemindersListViewModel(
                    getApplicationContext(),
                    get() as ReminderDataSource
                )
            }
            single {
                SaveReminderViewModel(
                    getApplicationContext(),
                    get() as ReminderDataSource
                )
            }
            single { RemindersLocalRepository(get()) as ReminderDataSource }
            single { LocalDB.createRemindersDao(getApplicationContext()) }
        }

        startKoin { modules(listOf(myModule)) }

        repository = get()

        runBlocking { repository.deleteAllReminders() }
    }

    @Test
    fun remindersListFragment_navigateToSaveReminder() {
        val fragmentScenario =
            launchFragmentInContainer<ReminderListFragment>(Bundle(), R.style.AppTheme)
        val navController = mock(NavController::class.java)

        fragmentScenario.onFragment {
            Navigation.setViewNavController(it.view!!, navController)
        }

        // perform click on add reminder FAB
        onView(withId(R.id.addReminderFAB)).perform(click())

        // ensure navigation to save reminder using ReminderListFragmentDirections
        verify(navController).navigate(ReminderListFragmentDirections.toSaveReminder())
    }

    @Test
    fun reminders_DisplayedInUi() {
        // save reminder
        runBlocking { repository.saveReminder(reminder) }
        launchFragmentInContainer<ReminderListFragment>(Bundle.EMPTY, R.style.AppTheme)

        // ensure title, description, and location are displayed
        onView(withText(reminder.title)).check(matches(isDisplayed()))
        onView(withText(reminder.description)).check(matches(isDisplayed()))
        onView(withText(reminder.location)).check(matches(isDisplayed()))
    }

    @Test
    fun noReminder_showNoData() {
        launchFragmentInContainer<ReminderListFragment>(Bundle.EMPTY, R.style.AppTheme)

        // delete all reminders
        runBlocking { repository.deleteAllReminders() }

        // ensure no data text is displayed
        onView(withId(R.id.noDataTextView)).check(matches(isDisplayed()))
    }
}